import UIKit

extension UIImage {

    class func placeholderAvatar() -> UIImage {
        return UIImage(named: "placeholderAvatar")!
    }
    
    class func placeholderBanner() -> UIImage {
        return UIImage(named: "placeholderBanner")!
    }
    
}
